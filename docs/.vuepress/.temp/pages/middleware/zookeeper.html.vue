<h4 id="zookeeper选举" tabindex="-1"><a class="header-anchor" href="#zookeeper选举"><span>Zookeeper选举</span></a></h4>
<ul>
<li>
<p>集群中只要有超过过半的机器是正常工作的，那么整个集群对外就是可用的</p>
</li>
<li>
<p>也就是说如果有2个zookeeper，那么只要有1个死了zookeeper就不能用了，因为1没有过半，所以2个zookeeper的死亡容忍度为0；
同理，要是有3个zookeeper，一个死了，还剩下2个正常的，过半了，所以3个zookeeper的容忍度为1，奇数个服务器是效率最高的。</p>
</li>
<li>
<p>ZooKeeper 集群服务器扮演了两个职能。它们一方面从客户端接受连接与操作请求，另一方面对操作结果进行投票。这两个职能在 ZooKeeper集群扩展的时候彼此制约。</p>
<ul>
<li>
<p>因为Leader节点必须等待集群中过半Server响应投票，于是节点的增加使得部分计算机运行较慢，从而拖慢整个投票过程的可能性也随之提高，写操作也会随之下降。这正是我们在实际操作中看到的问题——随着 ZooKeeper 集群变大，写操作的吞吐量会下降。</p>
</li>
<li>
<p>我们不得不得在增加Client数量的期望和我们希望保持较好吞吐性能的期望间进行权衡。要打破这一耦合关系，我们引入了不参与投票的服务器，称为Observer。 Observer可以接受客户端的连接，并将写请求转发给Leader节点。但是，Observer不参与投票过程，只是和其他服务节点一起得到投票结果。</p>
</li>
</ul>
</li>
</ul>
<h4 id="zookeeper的读写机制" tabindex="-1"><a class="header-anchor" href="#zookeeper的读写机制"><span>Zookeeper的读写机制</span></a></h4>
<ul>
<li>
<p>Zookeeper是一个由多个server组成的集群，一个leader,多个follower,每个server保存一份数据副本，全局数据一致，分布式读写，更新请求转发，由leader实施。</p>
</li>
<li>
<p>一个client的更新请求按其发送顺序执行，数据更新原子性，一次数据更新要么全成功，要么失败，client无论连接哪个server,数据视图都是一致的.</p>
</li>
</ul>
<h4 id="zookeeper节点数据操作流程" tabindex="-1"><a class="header-anchor" href="#zookeeper节点数据操作流程"><span>Zookeeper节点数据操作流程</span></a></h4>
<p><strong>操作流程图：</strong>
<img :src="$withBase('/apollo/4.png')" alt="dock"></p>
<ul>
<li>1.Client向Follwer发出一个写的请求</li>
<li>2.Follwer把请求发送给Leader</li>
<li>3.Leader接收到以后开始发起投票并通知Follwer进行投票</li>
<li>4.Follwer把投票结果发送给Leader</li>
<li>5.Leader将结果汇总后如果需要写入，则开始写入同时把写入操作通知给Leader，然后commit;</li>
<li>6.Follwer把请求结果返回给Client</li>
</ul>
<p><strong>Follower主要有四个功能：</strong></p>
<ul>
<li>
<ol>
<li>向Leader发送请求（PING消息、REQUEST消息、ACK消息、REVALIDATE消息）；</li>
</ol>
</li>
<li>2 .接收Leader消息并进行处理；</li>
<li>3 .接收Client的请求，如果为写请求，发送给Leader进行投票；</li>
<li>4 .返回Client结果。</li>
</ul>
<h3 id="zookeeper的设计目的" tabindex="-1"><a class="header-anchor" href="#zookeeper的设计目的"><span>zookeeper的设计目的</span></a></h3>
<ul>
<li>
<p>最终一致性：client不论连接到哪个Server，展示给它都是同一个视图，这是zookeeper最重要的性能。</p>
</li>
<li>
<p>可靠性：具有简单、健壮、良好的性能，如果消息被到一台服务器接受，那么它将被所有的服务器接受。</p>
</li>
<li>
<p>实时性：Zookeeper保证客户端将在一个时间间隔范围内获得服务器的更新信息，或者服务器失效的信息。但由于网络延时等原因，Zookeeper不能保证两个客户端能同时得到刚更新的数据，如果需要最新数据，应该在读数据之前调用sync()接口。</p>
</li>
<li>
<p>等待无关（wait-free）：慢的或者失效的client不得干预快速的client的请求，使得每个client都能有效的等待。</p>
</li>
<li>
<p>原子性：更新只能成功或者失败，没有中间状态。</p>
</li>
<li>
<p>顺序性：包括全局有序和偏序两种：全局有序是指如果在一台服务器上消息a在消息b前发布，则在所有Server上消息a都将在消息b前被发布；偏序是指如果一个消息b在消息a后被同一个发送者发布，a必将排在b前面。</p>
</li>
</ul>
